"""
Step 2: Extract needed images from COCO zip files.
Reads the image list files generated by prepare_bsd_streaming.py,
extracts only those images from the COCO zip archives.
"""
import zipfile
from pathlib import Path

COCO_DIR = Path("/autodl-pub/data/COCO2017")
OUTPUT_DIR = Path("/root/autodl-tmp/BSD/datasets/bsd")

def extract_split(split_name):
    zip_file = COCO_DIR / f"{split_name}2017.zip"
    list_file = OUTPUT_DIR / f"{split_name}_images.txt"
    out_img_dir = OUTPUT_DIR / split_name / "images"

    if not zip_file.exists():
        print(f"  ERROR: {zip_file} not found")
        return 0
    if not list_file.exists():
        print(f"  ERROR: {list_file} not found")
        return 0

    # Read image list
    with open(list_file) as f:
        needed = set(line.strip() for line in f if line.strip())
    print(f"  Need {len(needed)} images")

    out_img_dir.mkdir(parents=True, exist_ok=True)

    extracted = 0
    skipped = 0

    with zipfile.ZipFile(zip_file, 'r') as zf:
        zip_names = set(zf.namelist())

        for fname in needed:
            zip_path = f"{split_name}2017/{fname}"
            dst = out_img_dir / fname

            if dst.exists():
                extracted += 1
                continue

            if zip_path not in zip_names:
                skipped += 1
                continue

            zf.extract(zip_path, path=str(out_img_dir.parent))
            # Move from subdir
            src = out_img_dir.parent / zip_path
            if src.exists():
                src.rename(dst)
            extracted += 1

            if extracted % 5000 == 0:
                print(f"  Progress: {extracted}/{len(needed)}")

    # Cleanup empty dir
    subdir = out_img_dir.parent / f"{split_name}2017"
    if subdir.exists():
        import shutil
        shutil.rmtree(subdir, ignore_errors=True)

    print(f"  Done: {extracted} extracted, {skipped} not in zip")
    return extracted

if __name__ == "__main__":
    for s in ["train", "val"]:
        print(f"\n{'='*60}")
        print(f"Extracting {s}2017...")
        n = extract_split(s)
        print(f"  Total: {n} images")
    print(f"\nAll done. Images in: {OUTPUT_DIR}")
